import type { APIRoute } from 'astro';
import { db } from '../../../lib/db/client';
import { users } from '../../../lib/db/schema';
import { eq } from 'drizzle-orm';

export const POST: APIRoute = async ({ request, cookies }) => {
  try {
    const { email, password } = await request.json();

    if (!email || !password) {
      return new Response(
        JSON.stringify({ error: 'Email and password are required' }),
        { status: 400, headers: { 'Content-Type': 'application/json' } }
      );
    }

    // In production, use proper password hashing (bcrypt, etc.)
    const user = db.select().from(users).where(eq(users.email, email)).get();

    if (!user || user.password !== password) {
      return new Response(
        JSON.stringify({ error: 'Invalid credentials' }),
        { status: 401, headers: { 'Content-Type': 'application/json' } }
      );
    }

    // Set session cookie (in production, use secure session management)
    cookies.set('session', user.id.toString(), {
      path: '/',
      httpOnly: true,
      sameSite: 'lax',
      maxAge: 60 * 60 * 24 * 7, // 7 days
    });

    return new Response(
      JSON.stringify({ success: true, user: { id: user.id, name: user.name, email: user.email } }),
      { status: 200, headers: { 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    console.error('Login error:', error);
    return new Response(
      JSON.stringify({ error: 'Failed to login' }),
      { status: 500, headers: { 'Content-Type': 'application/json' } }
    );
  }
};

